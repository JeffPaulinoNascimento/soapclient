package br.com.soap.modelos;

public class PaisDatasus {
    private String codigoPais;
    private String codigoPaisAntigo;
    private String nomePais;

    public String getCodigoPais() {
        return codigoPais;
    }

    public void setCodigoPais(String codigoPais) {
        this.codigoPais = codigoPais;
    }

    public String getCodigoPaisAntigo() {
        return codigoPaisAntigo;
    }

    public void setCodigoPaisAntigo(String codigoPaisAntigo) {
        this.codigoPaisAntigo = codigoPaisAntigo;
    }

    public String getNomePais() {
        return nomePais;
    }

    public void setNomePais(String nomePais) {
        this.nomePais = nomePais;
    }
}
